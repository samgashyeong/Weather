package com.example.weather.data;

public class Coord {
    private float lon;
    private float lat;



    public float getLon() {
        return lon;
    }

    public float getLat() {
        return lat;
    }


    public void setLon(float lon) {
        this.lon = lon;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }
}
